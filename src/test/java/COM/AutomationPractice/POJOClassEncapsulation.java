package COM.AutomationPractice;

public class POJOClassEncapsulation {

	private String s="shanmuga";
	private String c="raja";
	private String a="QA";
	
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public String getC() {
		return c;
	}
	public void setC(String c) {
		this.c = c;
	}
	public String getA() {
		return a;
	}
	public void setA(String a) {
		this.a = a;
	}
	
	
	
	
}
